def menu():
    global day
    global name
    if 0<=level<=10:
        name="Bulbasaur"
    if 10<=level<=20:
        name="Ivysaur"
    if level>=20:
        name="Venusaur"
    choice=input(f"Today is Day {day}. What would you like to do with {name}?(train, battle, display info, FINAL BOSS):").lower()
    day=day+1
    if choice=="train":
        level_up()
    if choice=="battle":
        battle()
    if choice=="display info":
        info()
    if choice=="exit":
        exit()
    if choice=="final boss":
        finalboss()
        exit()
def level_up():
    global level
    level=level+1
    time.sleep(2)
    print(f"{name} has leveled up!")
    if level==10:
        print("Bulbasaur has evolved into Ivysaur")
        print_ivysaur()
    if level==20:
        print("Ivysaur has evolved into Venusaur")
        print_venusaur()
